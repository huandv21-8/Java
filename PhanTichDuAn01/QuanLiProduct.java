    private void setIDcategory() {
        for (Category category : listCategory) {
            cbidcategory.addItem(category.getiD_category());
        }
    }

    private void reset() {
        txtidproduct.setText("");
        txtnameproduct.setText("");
        txtprice.setText("");
        txtquantity.setText("");
        txtHsd.setText("");
        txtngaysx.setText("");
    }

    private void getData() {

        listproduct.clear();
        tableModel.setRowCount(0);
        listproduct = Manage.getProduct();
        listCategory = Manage.fillAll();

        for (Product sp : listproduct) {
            tableModel.addRow(new Object[]{sp.getIdProduct(), sp.getIdCategory(),
                sp.getNameProduct(), sp.getQuantity(), sp.getPrice(), sp.getDateManufacture(),
                sp.getExpiryDate()});
        }
    }

