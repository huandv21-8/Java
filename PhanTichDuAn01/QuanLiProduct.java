    private void btaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaddActionPerformed
        // TODO add your handling code here:
        String idproduct = txtidproduct.getText();
        if (idproduct.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Id product not is empty");
            return;
        }
        String idcategory = (String) cbidcategory.getSelectedItem();
        String nameproduct = txtnameproduct.getText();
        int quantity = Integer.parseInt(txtquantity.getText());
        int price = Integer.parseInt(txtprice.getText());
        String dateManufacture = txtngaysx.getText();
        String dateExpiry = txtHsd.getText();

        Product product = new Product(idproduct, idcategory, nameproduct, quantity, price, dateManufacture, dateExpiry);

        boolean ischeck = false;
        for (Product product1 : listproduct) {
            if (idproduct.equals(product1.getIdProduct())) {
                ischeck = true;
                break;
            }
        }
        if (ischeck) {
            JOptionPane.showMessageDialog(rootPane, "Id product đã tồn tại");
            return;
        }
        /// 
        Manage.insertProduct(product);
        getData();
        reset();
        JOptionPane.showMessageDialog(rootPane, "Inser thanh cong idproduct = " + idproduct);

    }//GEN-LAST:event_btaddActionPerformed

    private void tbViewSpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbViewSpMouseClicked
        // TODO add your handling code here:
        index = tbViewSp.getSelectedRow();
        txtidproduct.setText(listproduct.get(index).getIdProduct());
        txtnameproduct.setText(listproduct.get(index).getNameProduct());
        txtprice.setText(String.valueOf(listproduct.get(index).getPrice()));
        txtquantity.setText(String.valueOf(listproduct.get(index).getQuantity()));
        txtHsd.setText(listproduct.get(index).getDateManufacture());
        txtngaysx.setText(listproduct.get(index).getExpiryDate());
        cbidcategory.setSelectedItem(listproduct.get(index).getIdCategory());

    }//GEN-LAST:event_tbViewSpMouseClicked

    private void btupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btupdateActionPerformed
        // TODO add your handling code here:

        String idproduct = txtidproduct.getText();
        System.out.println("idproduct = ");
        if (idproduct.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Id product not is empty");
            return;
        }
        String idcategory = (String) cbidcategory.getSelectedItem();
        String nameproduct = txtnameproduct.getText();
        int quantity = Integer.parseInt(txtquantity.getText());
        int price = Integer.parseInt(txtprice.getText());
        String dateManufacture = txtngaysx.getText();
        String dateExpiry = txtHsd.getText();

        Product product = new Product(idproduct, idcategory, nameproduct, quantity, price, dateManufacture, dateExpiry);

        boolean ischeck = false;
        for (Product product1 : listproduct) {
            if (idproduct.equals(product1.getIdProduct())) {
                ischeck = true;
                break;
            }
        }
        if (ischeck) {
            Manage.updateProduct(product);
            JOptionPane.showMessageDialog(rootPane, "update thanh cong id product = " + idproduct);
            getData();
            reset();
            return;
        }
        JOptionPane.showMessageDialog(rootPane, " id product = " + idproduct + " không tồn tại");

    }//GEN-LAST:event_btupdateActionPerformed

    private void btdeletedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdeletedActionPerformed
        // TODO add your handling code here:

        String idproduct = txtidproduct.getText();
        if (idproduct.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Id product not is empty");
            return;
        }
        boolean ischeck = false;
        for (Product product1 : listproduct) {
            if (idproduct.equals(product1.getIdProduct())) {
                ischeck = true;
                break;
            }
        }
        if (ischeck) {
            Manage.deleteProduct(idproduct);
            JOptionPane.showMessageDialog(rootPane, "Xoa thanh cong product id = " + idproduct);
            getData();
            reset();
            return;
        }
         JOptionPane.showMessageDialog(rootPane, " product id = " + idproduct+" nhập sai không tồn tại");
    }//GEN-LAST:event_btdeletedActionPerformed

