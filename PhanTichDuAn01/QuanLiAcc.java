    private void reset() {
        txtIDuser.setText("");
        txtpass.setText("");
        txtusername.setText("");
        cbRole.setSelectedIndex(0);
        cbIDperson.setSelectedIndex(0);
    }

    private void readDataUser() {
        listUser.clear();
        String sql = "select * from users";
        listUser = manage.checkLogin(sql);
        tableModel.setRowCount(0);
        //add data len table
        for (User listUser1 : listUser) {
            tableModel.addRow(new Object[]{listUser1.getID_user(), listUser1.getUserName(),
                listUser1.getPassWord(), listUser1.getID_person(), listUser1.getRole()});
        }
        

    }

    private void setDataComboboxRole() {
        listPerson.clear();
        listPerson = manage.getPerson();
        for (Person person : listPerson) {
            cbIDperson.addItem(person.getID_person());
        }
    }

