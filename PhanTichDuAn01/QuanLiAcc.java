    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed

        //add user
        String iduser = txtIDuser.getText();
        if (iduser.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "ID user ko được để trống");
            return;
        }
        String username = txtusername.getText();
        String pass = txtpass.getText();
        String idperson = (String) cbIDperson.getSelectedItem();
        String role = (String) cbRole.getSelectedItem();

        boolean ischeck = false;
        User user = new User(iduser, username, pass, idperson, role);
        for (User listUser1 : listUser) {
            if (iduser.equals(listUser1.getID_user())) {
                ischeck = true;

                break;
            }
        }
        if (ischeck) {
            JOptionPane.showMessageDialog(rootPane, "ID user đã tồn tại");
            return;
        }
        manage.insertUser(user);
        readDataUser();
        reset();

    }//GEN-LAST:event_btAddActionPerformed

