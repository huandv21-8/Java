    private void tbAccMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAccMouseClicked
        // TODO add your handling code here:
        index = tbAcc.getSelectedRow();
        txtIDuser.setText(listUser.get(index).getID_user());
        txtusername.setText(listUser.get(index).getUserName());
        txtpass.setText(listUser.get(index).getPassWord());
        cbRole.setSelectedItem(listUser.get(index).getRole());
        cbIDperson.setSelectedItem(listUser.get(index).getID_person());

    }//GEN-LAST:event_tbAccMouseClicked

    private void btDeletedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeletedActionPerformed
        // TODO add your handling code here:
        String iduser = txtIDuser.getText();
        if (iduser.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nhap id user can xoa");
            return;
        }
        boolean ischeck = false;
        for (User us : listUser) {
            if (us.getID_user().equals(iduser)) {
                ischeck = true;
                break;
            }
        }
        if (ischeck) {
            Manage.deleteUser(iduser);
            JOptionPane.showMessageDialog(rootPane, "Xoa thanh cong id user = " + iduser);
            readDataUser();
            reset();
            return;
        }
        JOptionPane.showMessageDialog(rootPane, "user = " + iduser + " không tồn tại");
        reset();
    }//GEN-LAST:event_btDeletedActionPerformed

