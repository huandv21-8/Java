    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        // TODO add your handling code here:
        String iduser = txtIDuser.getText();
        String username = txtusername.getText();
        String pass = txtpass.getText();
        String role = (String) cbRole.getSelectedItem();
        String idperson = (String) cbIDperson.getSelectedItem();

        User us = new User(iduser, username, pass, idperson, role);

        if (iduser.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ID user not empty");
            return;
        }
        if (iduser.equals(listUser.get(index).getID_user())) {
            Manage.updateUser(us);
            JOptionPane.showMessageDialog(rootPane, "update thanh cong iduser = " + iduser);
            readDataUser();
            reset();
            return;
        }
        JOptionPane.showMessageDialog(rootPane, "ID user khong the thay doi");

    }//GEN-LAST:event_btUpdateActionPerformed

