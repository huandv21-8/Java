    public void showTable() {
        int no1 = 1;
        for (Withdrawal listWithdrawal1 : listWithdrawal) {
            defaultTableModel.addRow(new Object[]{
                no1,
                listWithdrawal1.getAmountMoneyWithdrawal() + " vnđ",
                listWithdrawal1.getCharge() + " vnđ",
                listWithdrawal1.getDate()
            });
            no1++;
        }
        int no = 1;
        for (Transfers listTransferse : listTransferses) {
            defaultTableModel1.addRow(new Object[]{
                no,
                listTransferse.getBankTransfers(),
                listTransferse.getNameAccountTransfers(),
                listTransferse.getAccountNumberTransfers(),
                listTransferse.getAmountMoneyTransfers() + " vnđ",
                listTransferse.getCharge() + " vnđ",
                listTransferse.getDate()
            });
            no++;
        }
    }

