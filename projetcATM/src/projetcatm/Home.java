    private void SeeBalanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeeBalanceActionPerformed

        double tienRut = Double.parseDouble(txtAmountNumberMoneyWithdrawal.getText());
        if (0 < tienRut && tienRut <= 10000000) {
            float phi = (float) (tienRut * 0.05);
            txtChangeWithdrawal.setText(String.valueOf(phi));
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "The withdrawal amount must be greater than 0 vnđ and smaller than 10,000,000 vnđ");
        }
    }//GEN-LAST:event_SeeBalanceActionPerformed


