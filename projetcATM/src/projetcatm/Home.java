    private void SaveChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveChangeActionPerformed

        boolean checkPass = false;
        for (User listUser : listUser) {
            String pass = String.valueOf(listUser.getPass());
            if (pass.equalsIgnoreCase(txtOldPass.getText())) {
                checkPass = true;
                break;
            }
        }

        if (checkPass) {
            if (txtNewPass.getText().length() != 6) {
                JOptionPane.showMessageDialog(rootPane, "Password must be exactly six digits");
            } //            else if (txtNewPass.getText().matches("{0,9}")) {
            //                JOptionPane.showMessageDialog(rootPane, "Password must be number");
            //            }
            else {

                if (txtNewPass.getText().equals(txtEnterNewPass.getText())) {

                    int pass = Integer.parseInt(txtNewPass.getText());

                    User user = new User(pass);
                    manage.updateUser(user);
                    JOptionPane.showMessageDialog(rootPane, "Update succesful");

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Enter the wrong new password");
                }
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Enter the wrong old password");
        }

        txtOldPass.setText(null);
        txtNewPass.setText(null);
        txtEnterNewPass.setText(null);

    }//GEN-LAST:event_SaveChangeActionPerformed

